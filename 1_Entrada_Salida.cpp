#include <iostream>
using namespace std;

int main(){
    // cout: se usa para mostrar algo en consola. 
    cout << "Hola Mundo";

    // endl: se usa al final del cout, es un enter para la consola.
    cout << "Hola Mundo" << endl;
    
    // cin: se usa para ingresar un valor mediante consola.
    int num;
    cin >> num;
}